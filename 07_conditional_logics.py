# Logical operators are either "True or Flase" or "Yes or No" or "0 or 1"
# equal to          ==
# not equal to      !=
# less than         <
# greater than      >
# less than equal   <=
# greater than equal   >=


# is 5 equal to 4 ?
print(5==4)
print(5!=4)
print(5<4)
print(5>4)
print(5<=4)
print(5>=4)

